## Conversor de Temperatura

# Celsius para Fahrenheint

valor_celsius = int(input('Informe a temperatura em Celsius: ' )) 
print ((9/5) * valor_celsius + 32)

# Fahrenheint para Celsius

valor_fahrenheit = int(input('Informe a temperatura em Fahrenheit: ' )) 
print ((valor_fahrenheit - 32) * 5 / 9)